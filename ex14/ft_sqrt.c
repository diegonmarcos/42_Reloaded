int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0)
	{
		while ((i * i) <= nb)
			i++;
		i--;
		if ((nb - i * i) == 0)
			return (i);
		return (0);
	}
	return (0);
}
